const mongoose = require('mongoose')

// Create Schema
const EmployeeSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  department: {
    type: String,
    required: true
  },
  start_date: {
    type: Date,
    required: true
  },
  dob_date: {
    type: Date,
    required: true
  },
  street: {
    type: String,
    required: true
  },
  city: {
    type: String,
    required: true
  },
  state: {
    type: String,
    required: true
  },
  zipcode: {
    type: String,
    required: true
  }
});

module.exports = mongoose.model('Employee', EmployeeSchema)
